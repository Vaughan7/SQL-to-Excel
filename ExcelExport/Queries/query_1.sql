SELECT 	
	 CASE 

       WHEN GROUPING(MAJOR_DIV_CODE) = 1 THEN 'Grand_Total'

       ELSE MAJOR_DIV_CODE 

       END AS Divisions,
       
	 SUM(CASE WHEN ORG_CAT_ENT_CD = '21' THEN 1 ELSE 0 END) AS "21",
       SUM(CASE WHEN ORG_CAT_ENT_CD = 'CC' THEN 1 ELSE 0 END) AS "CC",
       SUM(CASE WHEN ORG_CAT_ENT_CD = 'FOR' THEN 1 ELSE 0 END) AS "FOR",
       SUM(CASE WHEN ORG_CAT_ENT_CD = 'CY' THEN 1 ELSE 0 END) AS "CY",
       COUNT(*) AS Grand_Total

FROM ICRS_INTERFACE.VW_ALL_ENTITY
GROUP BY ROLLUP (MAJOR_DIV_CODE)